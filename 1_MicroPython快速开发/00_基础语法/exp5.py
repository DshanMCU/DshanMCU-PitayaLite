# 大于>
a=200>100
print("200>100 is:", a)

# 小于>
a=200<100
print("200<100 is:", a)

# 大于等于>=
a=200>=100
print("200>=100 is:", a)

# 小于等于<=
a=200<=100
print("200<=100 is:", a)

# 等于==
a=200==100
print("200==100 is:", a)

# 不等于！=
a=200!=100
print("200!=100 is:", a)

